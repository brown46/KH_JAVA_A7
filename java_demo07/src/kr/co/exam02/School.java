package kr.co.exam02;

public class School {
	
	private int grade;
	private int cls;
	public School(int grade, int cls){
		this.grade=grade;
		this.cls=cls;
	}
	
	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public int getCls() {
		return cls;
	}

	public void setCls(int cls) {
		this.cls = cls;
	}

	
	


}
